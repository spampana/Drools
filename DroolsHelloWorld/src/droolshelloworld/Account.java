package droolshelloworld;

public class Account {
private String  id;
private int  balance;
private String name;


public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}
public String getId() {
	return id;
}
public void setId(String id) {
	this.id = id;
}
public int getBalance() {
	return balance;
}
public void setBalance(int balance) {
	this.balance = balance;
} 

	
}
